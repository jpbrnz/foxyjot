<template>
<a class="button-social" :class="buttonSocialDesignObject" :page-url="page_url" :page-title="page_title" :page-description="page_description" :button-design="button_design" :title-social="title_social" :has-icon="has_icon" :has-square-edges="has_square_edges"
  :has-counter="has_counter" @click.prevent="showShareWindow">
    <i class="icon-linkedin" v-if="this.$props.has_icon"></i>
    <span class="title-social" v-if="this.$props.title_social">{{ title_social }}</span>
  </a>
</template>

<script>
import defaultHref from './defaultHref'
// Variables
const description = document.querySelector('meta[name="description"]')

export default {
  name: 'LinkedInShare',
  props: {
    page_url: {
      type: String,
      default: defaultHref
    },
    page_title: {
      type: String,
      default: document.title
    },
    page_description: {
      type: String,
      default: (description) ? description.content : ''
    },
    button_design: {
      type: String,
      default: 'flat'
    },
    title_social: String,
    has_icon: Boolean,
    has_square_edges: Boolean,
    has_counter: Boolean
  },
  data () {
    return {
      buttonSocialDesignObject: {
        'button-social__square_edges': this.$props.has_square_edges,
        'linkedin__design__flat': this.$props.button_design === 'flat',
        'linkedin__design__gradient': this.$props.button_design === 'gradient',
        'linkedin__design__outline': this.$props.button_design === 'outline'
      },
      counter_linkedin: 0
    }
  },
  methods: {
    /**
     * Get a random integer between `min` and `max`.
     *
     * @param {number} min - min number
     * @param {number} max - max number
     * @return {number} a random integer
     */
    getRandomInt: (min, max) => {
      return Math.floor(Math.random() * (max - min + 1) + min)
    },

    /**
     * Slice thousand integer and add `k` letter.
     *
     * @param {number} number - thousand integer
     * @return {string} a integer with letter
     */
    sliceThousandInt: (number) => {
      return (number / 1000).toFixed(1) + 'k'
    },

    /**
     * Show share window.
     *
     * @return {object} a pop-up window
     */
    showShareWindow: function () {
      // Variables
      const width = 640
      const height = 640
      let left = (screen.width / 2) - (width / 2)
      let top = (screen.height / 2) - (height / 2)
      const windowConfig = 'width=' + width + ',height=' + height + ',left=' + left + ',top=' + top + ','
      const shareUrl = 'https://www.linkedin.com/shareArticle?' +
        'url=' + encodeURIComponent(this.$props.page_url) +
        '&text=' + encodeURIComponent(this.$props.page_title) +
        '&summary=' + encodeURIComponent(this.$props.page_description) +
        '&mini=true'

      return window.open(shareUrl, 'Share this', windowConfig + 'toolbar=no,menubar=no,scrollbars=no')
    }
  }
}
</script>

